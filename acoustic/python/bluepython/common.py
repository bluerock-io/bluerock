def reload_unsafe_modules():
    # workaround for `from builtins import open as _builtin_open` in some standart libraries (bz2, tarfile, tokenize)

    unsafe_modules = ["bz2", "tokenize", "tarfile"]
    for mod in unsafe_modules:
        if sys.modules.get(mod) is not None:
            # we use del instead of importlib.reload() because reload
            # does not seem to work with some version of python (3.8 and Path.resolve)
            del sys.modules[mod]


